CREATE TABLE Tenders (
	TenderSiteURL VARCHAR(75),
	TenderSiteName VARCHAR(75),
	OrgChain VARCHAR(250),
	Refno VARCHAR(75),
	Webid VARCHAR(30),
	TenderType VARCHAR(30),
	FormofContract VARCHAR(30),
	TenderCategory VARCHAR(30),
	Covers INT,
	TechEval TINYINT(1),
	ItemwiseTechEval TINYINT(1),
	PaymentMode VARCHAR(10),
	BOQMultiCurr TINYINT(1),
	FeeMultiCurr TINYINT(1),
	TwoStageBid TINYINT(1),
	Tenderamount FLOAT,
	FeePayTo VARCHAR(100),
	FeePayAt VARCHAR(100),
	FeeExemp TINYINT(1),
	EMDamount FLOAT,
	EMDExemp TINYINT(1),
	EMDFeeType VARCHAR(20),
	EMDPercent FLOAT,
	EMDPayTo VARCHAR(100),
	EMDPayAt VARCHAR(100),
	Title VARCHAR(300),
	WorkDescrip VARCHAR(300),
	PreQual TEXT,
	External TEXT,
	Value FLOAT,
	Category VARCHAR(100),
	SubCategory VARCHAR(100),
	ContractType VARCHAR(30),
	BidValidity INT,
	PeriodofWork INT,
	Location VARCHAR(100),
	Pincode INT,
	PreBidMeetLoc VARCHAR(100),
	PreBidMeetAdd TEXT,
	PreBidMeetDate DATETIME,
	BidOpenLoc TEXT,
	AllowNDATender TINYINT(1),
	AllowPrefBid TINYINT(1),
	PubDate DATETIME,
	BidOpenDate DATETIME,
	DownloadStartDate DATETIME,
	DownloadEndDate DATETIME,
	ClariStartDate DATETIME,
	ClariEndDate DATETIME,
	BidSubStartDate DATETIME,
	BidSubEndDate DATETIME,
	NameofTenderer VARCHAR(100),
	AddressofTenderer TEXT,
	Currency VARCHAR(30),
	PRIMARY KEY (Webid));


